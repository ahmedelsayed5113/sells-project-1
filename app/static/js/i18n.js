/* ─── Helpers ───────────────────────────────────────────────────────── */
function getLang() { return localStorage.getItem("ain_lang") || "ar"; }
function setLang(lang) { localStorage.setItem("ain_lang", lang); applyLang(); }

function t(key, fallback) {
  const lang = getLang();
  const val = I18N[lang] && I18N[lang][key];
  if (val != null) return val;
  const ar = I18N.ar[key];
  if (ar != null) return ar;
  return fallback != null ? fallback : key;
}

/* Map a backend error response ({error_code, error}) to a localised string. */
function tError(err) {
  if (!err) return t("common.error");
  // err can be Error with .data, or a plain object, or a string
  let code = null;
  if (typeof err === "string") return err;
  if (err.data && err.data.error_code) code = err.data.error_code;
  else if (err.error_code) code = err.error_code;
  if (code) {
    const key = "errors." + code;
    const lang = getLang();
    if (I18N[lang] && I18N[lang][key]) return I18N[lang][key];
  }
  // fallback to generic message
  if (err.message) return err.message;
  if (err.error) return err.error;
  return t("common.error");
}

let _applyLangRunning = false;

function applyLang(skipCallback) {
  if (_applyLangRunning) return;
  _applyLangRunning = true;
  try {
    const lang = getLang();
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
    document.body.classList.toggle("lang-en", lang === "en");
    document.body.classList.toggle("lang-ar", lang === "ar");
    document.documentElement.className = lang === "en" ? "lang-en" : "";

    document.querySelectorAll("[data-i18n]").forEach(el => {
      el.textContent = t(el.getAttribute("data-i18n"));
    });
    document.querySelectorAll("[data-i18n-html]").forEach(el => {
      el.innerHTML = t(el.getAttribute("data-i18n-html"));
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
      el.setAttribute("placeholder", t(el.getAttribute("data-i18n-placeholder")));
    });
    document.querySelectorAll("[data-i18n-title]").forEach(el => {
      el.setAttribute("title", t(el.getAttribute("data-i18n-title")));
    });
    document.querySelectorAll("[data-i18n-aria-label]").forEach(el => {
      el.setAttribute("aria-label", t(el.getAttribute("data-i18n-aria-label")));
    });
    document.querySelectorAll("[data-i18n-value]").forEach(el => {
      el.value = t(el.getAttribute("data-i18n-value"));
    });

    const btn = document.getElementById("langToggleBtn");
    if (btn) btn.textContent = lang === "ar" ? "EN" : "AR";

    if (!skipCallback && typeof onLangChange === "function") {
      try { onLangChange(lang); } catch (e) { console.error(e); }
    }
  } finally {
    _applyLangRunning = false;
  }
}

document.addEventListener("DOMContentLoaded", () => applyLang());
